										<h4>1. Prospect and Research to Create a List of Websites for
											Outreach</h4>
										<ul>
											<li>For each campaign we create a list of over 200 qualified
												websites to target.</li>
											<li>We manually search Google for sites that accept guest posts
												with a variety of fine-tuned search queries.</li>
											<li>While prospecting, we already start qualifying websites:<ul>
													<li>Are the links in the blog post main body content
														&#8220;dofollow&#8221;?</li>
													<li>Has this website published blog posts within the
														last few weeks or stopped posting new content on
														their blog?</li>
													<li>What is the site&#8217;s domain authority?</li>
													<li>Are there submission guidelines available for
														contributors?</li>
													<li>Does this site actually still accept guest posts?
													</li>
													<li>Is this site a subdomain or a root domain?</li>
													<li>What is the topic of this website and is it relevant
														to your website?</li>
												</ul>
											</li>
										</ul>
										<h4>2. Collect Website Data and Contact Details</h4>
										<p>For each website, we manually collect the following information
											and combine it into a master list while removing any duplicated
											sites:</p>
										<ol>
											<li>website address</li>
											<li>website name</li>
											<li>domain authority</li>
											<li>blog url</li>
											<li>name of contact person</li>
											<li>email address of contact person</li>
											<li>link to submission guidelines</li>
											<li>topic/niche focus</li>
											<li>contact us page</li>
										</ol>
										<p>If no email address can be found for the website, we use several
											alternative means to manually try to find it for direct
											communication with the website.</p>
										<h4>3. Re-Qualify Prospects Again</h4>
										<p>After our list of 200+ sites for your campaign is created, we
											take a closer look at each website before reaching out to them.
											 We want to make sure that they meet all of our criteria, being
											careful that this is a high-quality website that can be trusted.
										</p>
										<ol>
											<li>a manual site inspection to get familiar with the site and
												what it is about</li>
											<li>the domain authority and if it meets our minimum</li>
											<li>the site&#8217;s spam score according to Moz</li>
											<li>new blog posts have recently been published (the site and
												blog are still active)</li>
											<li>links in the article are dofollow</li>
											<li>the topic of this website is relevant to our website</li>
											<li>the website is real, legitimate, and has &#8220;real
												company&#8221; attributes</li>
										</ol>
										<h4>4. Outreach and Follow-Up</h4>
										<p>For every 100 emails that we send out, we may hear back from
											between 5 and 20 of them, giving us a response rate of only
											5%-20%.  This is normal in outreach for SEO.  Many website
											owners, bloggers, and editors get bombarded daily with emails
											while they are busy running their businesses.  To increase our
											chances of connecting with the website owner to get a guest post
											published on their site, we do the following:</p>
										<ul>
											<li>Personalize every email (no templates) and build a real
												relationship with the website owner.  Mass emails are
												impersonal and don&#8217;t get responses, hence SEO is
												intrinsically &#8220;unscalable&#8221;.</li>
											<li>Pitch 3 creative article ideas that their audience would
												love and would be a great fit for their site.</li>
											<li>Test different subject lines to get bloggers opening our
												emails.</li>
											<li>Follow up continuously every 3-5 business days until we hear
												back or we have sent up to 4 emails and consider them a
												&#8220;dead lead&#8221;.</li>
											<li>We track every step of the process in a database with
												entries for each website to stay organized</li>
										</ul>
										<h4>5. Write The Guest Post</h4>
										<ol>
											<li>Review the website&#8217;s submission guidelines to see what
												requirements are needed for a guest post to be accepted and
												published.</li>
											<li>Create a writing assignment for a blog post to be written
												with all relevant detailed information.</li>
											<li>An American writer comes up with a the concept and idea for
												the blog post.</li>
											<li>We create an outline.</li>
											<li>Write the blog post.</li>
											<li>Review the first draft of the blog post, proof-read it, and
												edit it</li>
											<li>Naturally include the backlink in the blog post to ensure
												that it is properly placed, the link works correctly, and
												has the correct anchor text variation.</li>
										</ol>
										<h4>6. Submit The Guest Post</h4>
										<ol>
											<li>The blog post is submitted to the website.</li>
											<li>If the blog post is rejected for any reason, we modify the
												blog post with requested changes and re-submit it a second
												time.</li>
											<li>We follow up if we don&#8217;t hear back from the site owner
												to see when it will be published.</li>
										</ol>
										<h4>7. Confirm The Backlink</h4>
										<ol>
											<li>After the blog post is published and live, we verify that
												the backlink has been included and that it is a dofollow
												backlink.</li>
											<li>We track the published guest post as a live link.</li>
										</ol>
