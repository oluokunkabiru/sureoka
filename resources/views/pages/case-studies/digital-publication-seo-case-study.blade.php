									<div class="fusion-text fusion-text-5" style="text-transform:none;">
										<h3>A Brand New Site in an Extremely Competitive Landscape</h3>
										<p>The digital publication partnered with Sure Oak soon after
											launching its new website, which had only 15 pages.</p>
										<p>From the beginning, there were a few major challenges to
											confront. First off, the website was entering the extremely
											competitive landscape of digital publications and needed to
											establish itself as a worthy contender and an authoritative
											source for thought-provoking, personal stories.</p>
										<p>To make things even more difficult, the content the site was
											publishing included an extremely wide range of topics, which
											isn&#8217;t exactly ideal for SEO. That meant it needed a
											strategy that would not only ensure each story ranked for
											relevant keyword phrases but that the homepage and other
											high-level pages ranked for terms related to its service
											offerings — e.g. anonymous stories.</p>
										<h3>Technical SEO Challenges and Keeping Pace with Growth</h3>
										<p>There were also some initial technical challenges, specifically
											related to architecture and page speed.</p>
										<p>The original architecture that had been built out did not account
											for what would eventually be needed to keep pages organized
											— i.e. as the site grew, organization would likely become
											problematic.</p>
										<p>In addition, the site speed was very slow, especially considering
											the fact the site — at the time — had only 15 pages. We knew the
											site speed would only get worse as the site grew.</p>
										<p>So at the beginning of the program, it was imperative that Sure
											Oak not only establish a comprehensive on-page and off-page
											strategy; it was just as important to address the underlying
											technical issues on the website to ensure those efforts were not
											affected by crawl errors or low site speed scores.</p>
									</div>
									<style type="text/css"></style>
									<div class="fusion-title title fusion-title-5 fusion-sep-none fusion-title-center fusion-title-text fusion-title-size-one"
										style="margin-top:0px;margin-right:0px;margin-bottom:31px;margin-left:0px;">
										<h1 class="title-heading-center" style="margin:0;color:#007499;">
											Solution</h1>
									</div>
									<div class="fusion-text fusion-text-6" style="text-transform:none;">
										<p>Sure Oak designed a unique and specialized strategy for
											optimizing narratives in order to target relevant keywords and
											phrases without compromising or adulterating the existing
											content and remain true to the website&#8217;s mission of
											unfiltered narratives, while preserving each author&#8217;s
											intent and voice.</p>
										<p>Despite constraints — e.g. predetermined page topics, limited
											editorial input, etc. — around optimizing content on the site,
											Sure Oak was able to vastly improve visibility in search engine
											results pages.</p>
										<p>Examples of successfully optimized pages from their publish date
											to their current status include:</p>
										<p><b>Narrative: </b>I Had an Affair With My Best Friend&#8217;s
											Husband<br /> <b>Results:</b> From six to more than 2,000
											sessions per month a year later</p>
										<p><b>Narrative:</b> Why Do I Feel Guilty After Sex? My Christian
											Upbringing, That&#8217;s Why<br /> <b>Results:</b> From one
											session to more than 600 sessions per month a year later</p>
										<p><b>Narrative:</b> I Love Someone I&#8217;ve Never Met<br />
											<b>Results:</b> From one session to more than 700 sessions per
											month a year later</p>
										<p><b>Narrative:</b> Life as a Female Police Officer in 2020<br />
											<b>Results:</b> From 0 sessions to almost 400 sessions per month
											less than a year later</p>
										<p><b>Narrative:</b> Kamala Harris and Sex Workers: Can They Work
											Together in a Biden Presidency?<br /> <b>Results: </b>From 0
											sessions to almost 300 sessions per month less than a year later
										</p>
										<p>Optimizing these narratives required a unique approach to keyword
											research and targeting. Intent played a much larger role, as
											most narratives touched on several topics. In order to determine
											which topic was most relevant to users, Sure Oak had to identify
											the most prominent theme within each narrative and build the
											keyword targeting around that theme. By consistently aligning
											narratives with searchers&#8217; intent, Sure Oak was able to
											improve rankings and build the keyword portfolio of the site.
										</p>
