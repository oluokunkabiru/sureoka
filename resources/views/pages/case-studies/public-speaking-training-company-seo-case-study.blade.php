									<div class="fusion-text fusion-text-6" style="text-transform:none;">
										<h3>Growing Organic Visibility in a Competitive Market</h3>
										<p>The first challenge was to identify the best organic search
											opportunities in what is a very competitive market. The online
											landscape for corporate public speaking and media training is
											dominated by companies with an established presence in organic
											search and brand recognition.</p>
										<p>Although Throughline Group had an established client list, the
											company needed to invest in its online presence in order to keep
											pace with the marketing efforts of competitors. And since
											they&#8217;re based in New York City &#8211; and because their
											engagement with Sure Oak started before the coronavirus pandemic
											&#8211; the goal was to increase visibility locally to reach
											prospects who could attend workshops in person.</p>
										<p>But, not long after our partnership began, we had to
											significantly adjust our strategy to account for the pandemic
											and gain visibility outside the New York City area in order to
											capitalize on virtual workshops.</p>
										<h3>Pivoting Strategies Due to the Pandemic</h3>
										<p>COVID-19 caused many businesses to pivot to a more digitally
											focused form of products and services. Throughline Group
											actively pursued a restructure of their business to transform
											in-person classes into virtual workshops to meet the rising
											demand of a socially distanced population.</p>
										<p>This transition required a huge pivot from our original
											locally-focused SEO strategy to expand the site&#8217;s keyword
											portfolio outside of terms specific to New York City. Most
											important was earning keyword rankings related to virtual and
											online offerings — even in the public speaking and media
											training space.</p>
										<p>That meant competing with businesses that were already
											established nationwide as well as every other business that was
											attempting to do the same thing at that time. Sure Oak guided
											Throughline Group through the transition quickly and
											successfully. And they remained a client kong after the
											transition.</p>
