										<h3>Working Through YMYL and EAT Google Penalties</h3>
										<p><span style="font-weight: 400;">The e-commerce supplement
												business</span> came to Sure Oak for two reasons:</p>
										<ol>
											<li>They felt they had hit a wall with their organic search
												traffic</li>
											<li>They believed they had been hit by a Google penalty for
												violating Your Money or Your Life (YMYL) or Expertise,
												Authoritativeness, and Trustworthiness (EAT) guidelines</li>
										</ol>
										<p>That first problem &#8211; hitting a wall with organic traffic
											&#8211; is one of the most common reasons businesses partner
											with an expert SEO agency. But the second problem &#8211; being
											hit by a Google penalty &#8211; takes a particularly high level
											of skill and experience to turn correct.</p>
										<p>Oftentimes, when businesses are hit by a penalty, they&#8217;re
											lucky to even recover the traffic they lose as a result of being
											penalized. With this healthcare e-commerce website, we
											didn&#8217;t just recover what was lost; we brought in more
											traffic, and <b>we increased their year-over-year revenue from
												organic search by more than $200,000.</b> We also did it in
											the middle of an unprecedented, global pandemic.</p>
										<h3>Getting Results During the Coronavirus Pandemic</h3>
										<p>Our engagement with the company began at the beginning of 2020.
											We were only three months into the campaign when the coronavirus
											essentially shut down the entire world.</p>
										<p>Fortunately, our client did not rely on brick-and-mortar stores
											to sell their products. However, dietary supplements are not
											covered by health insurance. They are, essentially, a luxury
											that not everyone can afford. And the people who can afford them
											are dependent on their disposable income to pay for them. But
											when the world shut down, millions of Americans and others all
											over the world lost their jobs, and that required them to limit
											any unnecessary spending.</p>
										<p>We not only helped the healthcare e-commerce website weather the
											storm; <b>we helped them continue to grow and generate 1,200
												more organic transactions than the previous year over a
												6-month period.</b></p>
										<h3>Targeting Unbranded Traffic</h3>
										<p>Dr. Carolyn Dean had name recognition in the holistic medicine
											and dietary supplement industry long before she ever founded the
											e-commerce supplement business. So when she did, anyone who saw
											her name next to the name of the brand instantly trusted them
											and became a potential customer.</p>
										<p>Like all great brands, this worked in their favor, for a while.
											Eventually, organic traffic flat-lined, and then the website was
											hit hard because it possibly wasn&#8217;t adhering to
											Google&#8217;s YMYL and EAT guidelines. So when we delved in
											during our initial SEO Game Plan research, we knew right away
											the website was going to have to target more general keyword
											phrases, as opposed to branded phrases, in order to achieve
											long-term results in organic search.</p>
										<p>Over the next year, we rolled out an extensive content strategy
											that included reoptimizing existing product pages, building new
											pages targeting topics specific to magnesium, and a slew of
											evergreen pages that users would benefit from forever. The
											result? Organic sessions increased. <b>Even better, new organic
												users increased 35 percent year-over-year over a 6-month
												period.</b></p>
