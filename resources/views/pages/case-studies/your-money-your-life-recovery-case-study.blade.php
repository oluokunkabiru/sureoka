											grow their business during the coronavirus pandemic, Sure Oak
											laid out a comprehensive SEO strategy from the beginning. It
											involved several components:</p>
										<ul>
											<li>Retargeting general keyword phrases, like &#8220;magnesium
												supplement,&#8221; on product pages</li>
											<li>Creating new content that proved to Google and users they
												were a credible source of information related to dietary
												supplements and building out pages to help users learn
												everything they need to know before buying dietary
												supplements</li>
											<li>Developing high-quality backlinks to relevant top-of-funnel
												pages to not only increase the website&#8217;s domain
												authority but also increase its referral traffic from
												reputable sites</li>
