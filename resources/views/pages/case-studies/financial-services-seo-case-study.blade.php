										<h3 style="color: #007499;">Finding an Agency That Provided Value
										</h3>
										<p>When AdvancePoint Capital contacted Sure Oak, the company already
											had worked with three other companies within the past year. They
											needed an agency that could genuinely improve their rankings in
											order to drive more leads from organic search. That’s why they
											chose to partner with Sure Oak.</p>
										<h3 style="color: #007499;">Shifting from PPC to SEO</h3>
										<p>Like a lot of clients we work with, AdvancePoint Capital wanted
											to reduce how much they were spending on PPC ads and divert more
											of their marketing budget to SEO. On top of that, PPC ads in
											their vertical are extremely costly — at the time this was
											written, the CPC for “small business loan” was nearly $73.</p>
										<h3 style="color: #007499;">Competing Against Industry Titans</h3>
										<p>As the fintech sector grows, more businesses are exploring
											alternatives to traditional sources of funding, like bank loans,
											which typically require trips to the bank, consultations and
											paperwork. This is where companies like AdvancePoint Capital
