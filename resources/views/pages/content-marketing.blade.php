										<h3>Part 2: Digital PR and Journalist Outreach</h3>
										<h4>1.  Build a Prospect List of Journalists for Outreach</h4>
										<p>We must get to know journalists, follow their beat, and appeal to
											their interests.  This can only be done by first finding the
											right journalists who would be interested in covering our story,
											and then reading their articles and understanding what content
											they like to publish.  Once we have compiled a data base of
											hundreds of journalists to pitch, we can then being outreach.
										</p>
										<h4>2.  Individually and Personally Pitch Qualified Journalists</h4>
										<p>No journalist wants to reply to a generic mass email from an
											email blast.  Journalists want real relationships with real
											people, and that&#8217;s our approach.  While significantly more
											time-consuming, the substantial increase in results from
											personalizing every email we send speak for themselves.</p>
										<h4>3.  Follow Up with Journalists and Track Coverage</h4>
										<p>It&#8217;s normal for us to not hear back from most of our
											initial emails.  Journalists are busy and working on many
											stories.  Friendly follow up emails in the coming weeks increase
											the odds of getting your content covered.</p>
