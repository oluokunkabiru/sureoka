										<h4>1. Ideation</h4>
										<p>Rather than guessing at what kinds of content or ideas might
											interest journalists, we research past, related pieces of
											content to see what has gotten the most buzz.  Piggybacking on
											past successful campaigns by other brands, marketers, and
											promoters, we brainstorm and create a newsworthy piece of
											content that has viral potential.  The final content piece
											should have wide appeal, be interesting, have an emotional hook,
											and be shareworthy.</p>
										<h4>2.  Execute Original Research</h4>
										<p>We perform original research by either creating a new survey,
											digging into your in-house data, or researching and curating
											existing sources of research and data.  Only by creating
											something new will your company become the source of citations.
											 When we have invested in and executed the necessary research,
											your business now becomes the authority and &#8220;original
											source for citation&#8221; to which other news outlets and
											websites will link back to.  We might additionally get quotes
											from a trusted expert to add credibility to the campaign.</p>
										<h4>3.  Create On-Page Content</h4>
										<p>Creating the final presentation of the original research is
											crucial for the campaign&#8217;s success.  With professional
											graphic design, we must use a combination of visuals that
											captivate, enticing copywriting, and if possible, interactive
											components.</p>
										<h4>4.  Create Embeddable &#8220;Assets&#8221; for Journalists to
											Share</h4>
										<p>Making it easier to journalists to share our findings makes it
											more likely that they&#8217;ll cover the story.  Having the
											right visuals and graphics to embed in their new article will
											make it that much easier for them to cover the story with a
											write up, and increase the odds of us earning that backlink.</p>
