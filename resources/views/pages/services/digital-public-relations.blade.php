										<h3>Digital PR FAQs</h3>
										<p><b>How does digital PR work?</b></p>
										<p>Digital PR is similar to traditional PR in the sense that it aims
											to drive brand awareness. The need for digital PR has emerged as
											businesses started generating  revenue and growing their name
											recognition online. At Sure Oak, we build relationships with
											editors, journalists and webmasters to get the word out about
											your business. This, in turn, drives referral traffic and
											increases brand awareness. </p>
										<p><b>How do we measure the success of Digital PR?</b></p>
										<p>We actively monitor your Google Analytics for referral traffic
											generated by our digital PR strategy. In addition, we monitor
											changes in SERPs to measure how digital PR has affected search
											rankings.Social listening tools help us see how many brand
											mentions, link shares and likes our strategy has generated as
											well. </p>
										<p><b>What is the difference between Digital PR and Link
												Building?</b></p>
										<p>Digital PR focuses on brand awareness, media hits and referral
											traffic with SEO benefits being a bonus, not the end goal. A lot
											of the backlinks resulting from a digital PR campaign would be
											&#8220;nofollow&#8221; links, meaning they would not impact
											search rankings.</p>
										<p>With a link building campaign, we&#8217;re aiming to generate as
											many high-quality dofollow backlinks, which do affect search
											rankings, to strategically chosen pages in order to the
											positions of those pages in SERPs and improve the overall
											authority of the website. </p>
										<p><b>Digital PR or Link Building?</b></p>
										<p>A Digital PR campaign cannot go on forever, but in order to
											remain competitive, you will need to consistently generate as
											many backlinks as possible. A <a
												href="link-building-services.html">link building
												campaign</a> can start after or at the same time as a
											digital PR campaign to give you a boost in rankings. Deciding
											between the two will also depend on your immediate goals. Are
											you after brand awareness and referral traffic or increased
											authority and rankings? </p>
										<h2>Online Reputation Management Services</h2>
										<p>Online reputation management (ORM) involves a combination of
											digital marketing, public relations and search engine
											optimization (SEO) strategies to promote and protect your
											business&#8217;s online image and reputation.</p>
										<h3>ORM vs SEO</h3>
										<p>The goal of SEO is to get a website to rank number one for as
											many relevant keywords as possible. The goal of ORM is to fill
											the first page of Google with content that&#8217;s targeting
											brand-focused keywords. Ultimately, ORM uses SEO principles and
											best practices to build an accurate portrayal of a company
											online and in SERPs.</p>
										<h3>ORM vs PR</h3>
										<p>On the surface, it may seem like ORM is just public relations for
											a business&#8217;s online presence because they have similar
											goals, namely brand management. However, ORM and digital PR 
											require the use of unique tactics to achieve different results.
										</p>
										<p>Public relations companies generally create trendy, buzzworthy
											content to redirect users&#8217; attention away from information
											that negatively affects a company&#8217;s image. The goal is to
											shift the public&#8217;s perception by flooding social media and
											news outlets with a new narrative. </p>
										<p>On the other hand, online reputation management aims to tell a
											more accurate and holistic story about a brand or individual by
											positioning specific stories and assets on the first page of
											Google.</p>
										<h3>Our ORM Process</h3>
										<ol>
											<li>Research, research and more research. Initially, we bury
												ourselves in information about you online. </li>
											<li>Finding and assessing vulnerabilities.</li>
											<li>Building a fortress of controllable assets.</li>
											<li>Expanding your online presence.</li>
											<li>Keeping our ear to the ground for any new brand mentions.
											</li>
											<li>Tweaking brand messaging and damage control, where needed.
											</li>
										</ol>
										<h3>ORM FAQs</h3>
										<p><b>What is and isn&#8217;t Online Reputation Management?</b></p>
										<p>It is: a great way to control the narrative for your brand
											online.<br />It isn&#8217;t: A magic wand to make all unwanted
											search results disappear forever.</p>
										<p>It is: A method to push new content to the first pages of
											SERPs. <br />It isn&#8217;t: A way to get rid of all legitimate
											bad reviews.</p>
										<p>With ORM, you control the narrative being told about your brand.
											From reviews management, to content promotion or legal
											initiatives, ORM gives you the tools to get the best image front
											and center.</p>
										<p>That said, ORM doesn&#8217;t prevent negative stories from coming
											out. Instead, <i>ORM is how you control what information users
												see first, and it&#8217;s accomplished by dominating search
												results with information you want them to see.</i></p>
