										<h2>How Inbound Marketing Works</h2>
										<p>What is inbound marketing? According to <a
												href="https://www.hubspot.com/inbound-marketing">HubSpot</a>:
										</p>
										<blockquote>
											<p><i>Inbound marketing is a business methodology that attracts
													customers by creating valuable content and experiences
													tailored to them. While outbound marketing</i>
												interrupts<i> your audience with content they don&#8217;t
													always want, inbound marketing forms connections they
													are looking for and solves problems they already
													have.</i></p>
										</blockquote>
										<p>To summarize, the idea is to attract, engage, and delight users
											(aka the HubSpot inbound marketing trio). And if you do that
											effectively, you&#8217;ll generate more repeat customers!</p>
										<h3>Inbound Marketing and SEO</h3>
										<p>At Sure Oak, there&#8217;s a synergy between what we do from an
											SEO standpoint and how we approach inbound marketing. Both SEO
											and inbound marketing prioritize the customer experience above
											all else.</p>
										<p>As Google&#8217;s search algorithms continuously evolve to be
											more like a human brain, customization has never been so
											important. And with inbound marketing, it&#8217;s everything.
										</p>
										<h2>SURE OAK&#8217;S VALUE ADD</h2>
										<p>With our inbound marketing approach, we don&#8217;t just produce
											top-of-the-funnel leads.  We guide users through the optimal
											customer journey once they land on your site.</p>
										<p>In short: <b>more organic traffic (SEO) + more inbound leads =
												more sales and revenue. </b></p>
										<p>Inbound marketing also involves storing all the minute details of
											every user&#8217;s journey in a CRM (Customer Relationship
											Management) software platform. Using a CRM, like <a
												href="https://www.hubspot.com/" target="_blank"
												rel="noopener noreferrer">HubSpot,</a> you can keep
											information related to customer behavior all in one place. That
											information can provide powerful insights about how users behave
											on your site that can inform pivots in your inbound marketing
											strategy and even your sales process.</p>
										<h3>Why Use HubSpot?</h3>
										<p>There&#8217;s a variety of CRM systems out there. But Hubspot is
											one of the more robust platforms. It not only helps you manage
											sales teams, but marketing and customer service processes as a
											whole.</p>
										<p>And, unlike other heavily sales-focused CRMs, Hubspot&#8217;s
											all-in-one platform has a single user interface that connects
											all customer-facing departments. HubSpot also packs a higher
											level of functionality and reporting when it comes to keeping
											track of customer details.</p>
										<p>In addition, HubSpot has the ability to integrate workflows
											across primary operations and automate several of what would
											otherwise be manual tasks. That way, your team can focus on what
											really matters most: helping your company run efficiently.</p>
										<p>If you&#8217;re worried about having to switch to another CRM
											down the road, HubSpot is uniquely designed to scale with your
											business as it grows. So whether you&#8217;re a five-person team
											or a large enterprise with over 500 employees, Hubspot offers a
											long-term CRM solution.</p>
